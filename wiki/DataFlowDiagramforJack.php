				<p>The data flow diagram follows the control flow diagram closely. Of course the focus is on the data that is being collected and passed through the program rather than the functions being performed.</p>

				<p>We start out with the schematic as before, which is stored in the system and analyzed. The data from that is used to make the tubes and caps. The tubes are then sent to the tube assembly function, and then passed back to the tube data bank. The new assembled tubes and caps come together in the next step and the assembled jack is stored and sent to be painted. And then the completed jack is returned to the user.</p>

				<p>Thus chart shows how the data is manipulated to go from a schematic to a 3D sculpture. There are many changes to the data along the way, which result in the end product.</p>


				<h2 class="sub-title">The artist James Love, and his life and thoughts</h2>
				<p>Jim Love (1927–2005) was born in Amarillo, Texas. He graduated from Baylor University in 1952 with a bachelor’s degree in business administration. After graduation, he moved to Houston. Love championed the practice of creating art out of the scrap metal he found in junkyards. he rose to prominence in 1961 when his groupings of found objects were included in the exhibition “The Art of Assemblage” at the Museum of Modern Art in New York.</p>

				<blockquote>“In my world there is less planning than might be thought,”  – Love</blockquote>

				<p>Jim Love’s sculptures first emerged after a trip to a Waco junkyard, where he found “huge mounds of junk 20 to 25 feet tall.” According to Love, “Within a few minutes, I thought I had discovered heaven.” From that point on, he began collecting bits and pieces of metal. He described his early sculptures as “put-togethers,” assemblages from found materials.</p>

				<blockquote>“You keep shuffling things and try to stay alert for whatever it is that might pop up because one thing might suggest something else”</blockquote>				


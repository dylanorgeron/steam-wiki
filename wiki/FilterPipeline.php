				<p>For the filter pipeline I started with an original image and continued adding filters until I came up with something that I was happy with. I wanted to add some texture and some darkness to the photograph to make the jack stand out a bit more. In the end I think it looks very painterly. It kind of transformed a physical, 3D piece of art into a more traditional 2D artwork. It could also be interesting to try turning it into other mediums, such as marble or cut paper.</p>

